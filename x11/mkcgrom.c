static void cpyank2cgrom(BYTE *dst, BYTE *src, int length) {

	int		y;

	while(length--) {
		for (y=16; y--;) {
			*dst++ = src[y*256];
		}
		src++;
	}
}

static void cpy12fnt2cgrom(BYTE *dst, BYTE *src, int bmpx, int bmpy) {

	int		i, j;

	bmpx /= 8;
	for (i=0; i<8; i++) {
		for (j=bmpy; j--;) {
			*(WORD *)dst = (*(WORD *)(&src[j*bmpx])) & 0xf0ff;
			dst += 2;
		}
		for (j=bmpy; j--;) {
			*dst++ = (src[j*bmpx+1] << 4) | (src[j*bmpx+2] >> 4);
			*dst++ = (src[j*bmpx+2] << 4);
		}
		src += 3;
	}
}

static void cpysmall2cgrom(BYTE *dst, BYTE *src, int line) {

	int		i, j;

	dst += 0x5e * 32 * line;
	for (i=0; i<0x5e; i++) {
		src += 2;
		for (j=16; j--;) {
			*(WORD *)dst = *(WORD *)(src + j * 256);
			dst += 2;
		}
	}
}

static void cpybig2cgrom(BYTE *dst, BYTE *src, int line) {

	int		i, j;

	dst += 0x5e * 72 * line;
	for (i=0; i<0x5e; i++) {
		src += 3;
		for (j=24; j--;) {
			*dst++ = *(src + j * 384 + 0);
			*dst++ = *(src + j * 384 + 1);
			*dst++ = *(src + j * 384 + 2);
		}
	}
}

